export function AppSidebar({ ...props }: React.ComponentProps<typeof Sidebar>) {
  const user = useAuthStore((state) => state.user);
  const accessToken = useAuthStore((state) => state.accessToken);
  const [topics, setTopics] = useState<any>([]);

  useEffect(() => {
    const fetchTopics = async () => {
      if (!accessToken) return;

      try {
        const response = await axios.get("http://localhost:8000/api/v1/topic/", {
          headers: {
            Authorization: `Bearer ${accessToken}`,
          },
          withCredentials: true,
        });
        console.log(response.data.data);
        setTopics(
          response.data.data.map((topic: any) => ({
            title: topic.title,
            description: topic.description,
            id: topic._id,
          })),
        );
      } catch (error: any) {
        console.log("Failed to fetch topics:", error.response?.data || error.message);
      }
    };

    fetchTopics();
  }, [accessToken]);

  return (
    <Sidebar collapsible="icon" {...props}>
      <SidebarHeader>
        <TeamSwitcher teams={data.teams} />
      </SidebarHeader>
      <SidebarContent>
        <NavProjects projects={topics} />
        <NavMain items={data.navMain} />
      </SidebarContent>
      <SidebarFooter>
        <NavUser user={user} accessToken={accessToken} />
      </SidebarFooter>
      <SidebarRail />
    </Sidebar>
  );
}
